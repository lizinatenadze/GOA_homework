# or operator

#       False   or  False
print( 83 > 105 or 11 == 17)   #false

#       true   or  false
print( 57 > 23 or 64 < 58)  #true

#       false   or  true 
print( 98 > 107 or 63 > 46)   #true

#        True   or  True
print( 55 == 55 or 99 > 98)   #true

#       False   or  False
print( 33 == 76 or 66 > 67)   #false


# and operator

#       True   and  True
print( 17 < 19 and 3 == 3)     #true

#       False  and  False
print( 19 > 27 and 54 == 13)      #false          

#       true   and  false
print( 31 < 52 and 55 > 71)      #false    

#       false  and  true 
print( 44 > 44 and 8 < 51)      #false

#       True   and  True
print( 62 == 62 and 36 < 39)     #true